use proconio::input;
use std::collections::BTreeMap;

trait Bound<T> {
    fn lower_bound(&self, x: &T) -> usize;
    fn upper_bound(&self, x: &T) -> usize;
}

impl<T: PartialOrd> Bound<T> for [T] {
    fn lower_bound(&self, x: &T) -> usize {
        let (mut low, mut high) = (0, self.len());
        while low + 1 < high {
            let mid = (low + high) / 2;
            if self[mid] < *x {
                low = mid;
            } else {
                high = mid;
            }
        }
        if self[low] < *x {
            low + 1
        } else {
            low
        }
    }

    fn upper_bound(&self, x: &T) -> usize {
        let (mut low, mut high) = (0, self.len());
        while low + 1 < high {
            let mid = (low + high) / 2;
            if self[mid] <= *x {
                low = mid;
            } else {
                high = mid;
            }
        }
        if self[low] <= *x {
            low + 1
        } else {
            low
        }
    }
}

fn main() {
    input! {n:usize,beans:[(u64,u64);n]}
    let mut memo = BTreeMap::new();
    for (delicious,color ) in beans {
        let min_delicious = memo.entry(color).or_insert(u64::MAX);
        *min_delicious = std::cmp::min(delicious, *min_delicious);
    }
    let mut v = memo.values().collect::<Vec<_>>();
    v.sort_unstable();
    println!("{}", v.last().unwrap());
}
